# Logger
# =========================================

def log_info(msg):

    print(f"ℹ️ INFO: {msg}")

def log_success(msg):

    print(f"✅ SUCCESS: {msg}")

def log_warning(msg):

    print(f"⚠️ WARNING: {msg}")

def log_error(msg):

    print(f"❌ ERROR: {msg}")
